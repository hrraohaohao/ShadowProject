package com.hao.lib.pluginManager.bean;

public class DManagerInfoBean {

    private int manager_version_code;
    private String manager_version_name;
    private String manager_url;

    public int getManager_version_code() {
        return manager_version_code;
    }

    public void setManager_version_code(int manager_version_code) {
        this.manager_version_code = manager_version_code;
    }

    public String getManager_version_name() {
        return manager_version_name;
    }

    public void setManager_version_name(String manager_version_name) {
        this.manager_version_name = manager_version_name;
    }

    public String getManager_url() {
        return manager_url;
    }

    public void setManager_url(String manager_url) {
        this.manager_url = manager_url;
    }
}
